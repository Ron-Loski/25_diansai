void UART_ParaData(uint8_t *Data, uint16_t len)
{
	for (uint16_t i = 0; i < len; i ++)
	{
		char ch = (char)Data[i];
		if (ch == '\n')
		{
			cmd_line[cmd_index] = '\0';
			
			UART_HandleLine(cmd_line);
			cmd_index = 0;
		}
		else if (ch == '\r')
		{
			
		}
		else
		{
			if (cmd_index < CMD_LINE_SIZE - 1)
			{
				cmd_line[cmd_index ++] = ch;
			}
			else
			{
				cmd_index = 0;
				memset(cmd_line, 0, CMD_LINE_SIZE);
			}
		}
	}
}

